package com.jiuray.uhf.entity;

/**
 * 
 * 读标签数据
 * */
public class ReadTagInfo {

	private String pc ;
	private String crc ;
	private String epc ;
	private String data ;
	
	
	public String getPc() {
		return pc;
	}
	public void setPc(String pc) {
		this.pc = pc;
	}
	public String getCrc() {
		return crc;
	}
	public void setCrc(String crc) {
		this.crc = crc;
	}
	public String getEpc() {
		return epc;
	}
	public void setEpc(String epc) {
		this.epc = epc;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	
}
